using System;
class CS09_Comments
{
    void Main()
    {
        // This is a single-line comment
        int x = 5;  // This is another single-line comment

        Console.WriteLine(x);

        /*
        This is a multi-line comment.
        It can span multiple lines and is useful
        for providing longer explanations or notes.
        */
        int y = 10;
        Console.WriteLine(y);
    }
}